package model.roadmap;

public class Achievement {
	private String name;
	private double points;
	public Achievement() {}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getPoints() {
		return points;
	}
	public void setPoints(double points) {
		this.points = points;
	}
}
